def search_for_range(array, target):
  ans = [-1, -1]
  start = False
  for x in range(len(array)):
    if start and array[x] == target:
      ans[1] = x
      if  x+1 == len(array) or array[x+1] == target:
        continue
      else:
        return ans
    if array[x] == target:
      ans[0] = x
      ans[1] = x
      start = True
  return ans
  
